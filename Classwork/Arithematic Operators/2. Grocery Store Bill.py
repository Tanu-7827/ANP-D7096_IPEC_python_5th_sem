'''Write a Python program to calculate the total cost of rice packets purchased.'''


# Input the number of rice packets
packets = int(input("Enter the number of rice packets: "))

# Input the cost of one rice packet
price_per_packet = float(input("Enter the cost of one rice packet: "))

# Calculate the total cost
total_cost = packets * price_per_packet

# Display the total cost
print("Total Cost =", total_cost)