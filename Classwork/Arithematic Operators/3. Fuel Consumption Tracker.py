# Program to find the average mileage of a car

# Input the total distance traveled (in km)
distance = float(input("Enter the distance traveled (in km): "))

# Input the fuel consumed (in liters)
fuel = float(input("Enter the fuel consumed (in liters): "))

# Calculate the average mileage
mileage = distance / fuel

# Display the average mileage
print("Average Mileage =", mileage, "km/l")