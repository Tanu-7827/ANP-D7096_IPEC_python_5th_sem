'''
Write a Python program to calculate the total monthly salary of an employee by adding the fixed salary 
and incentive amount. 
'''
# Input fixed salary from the user
fixed_salary = float(input("Enter fixed salary: "))

# Input incentive amount from the user
incentive = float(input("Enter incentive amount: "))

# Calculate total monthly salary
total_salary = fixed_salary + incentive

# Display the total monthly salary
print("Total Monthly Salary =", total_salary)