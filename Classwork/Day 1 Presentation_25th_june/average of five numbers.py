''' To calculate the average of 5 numbers '''

# Input of first number from the user
num1 = float(input("Enter the first number: "))

# Input of second number from the user
num2 = float(input("Enter the second number: "))

# Input of third number from the user
num3 = float(input("Enter the third number: "))

# Input of fourth number from the user
num4 = float(input("Enter the fourth number: "))

# Input of fifth number from the user
num5 = float(input("Enter the fifth number: "))

# -----------------------------------------------------

# Displaying data to the user
print("First Number:", num1)
print("Second Number:", num2)
print("Third Number:", num3)
print("Fourth Number:", num4)
print("Fifth Number:", num5)

# -----------------------------------------------------

# Calculating the average
average = (num1 + num2 + num3 + num4 + num5) / 5

# Displaying the average
print("Average of the 5 numbers:", average)
